class Appointment {
  final String appointmentNo;
  final String appointmentDate;
  final String status;
  final String paramedicName;
  final String appointmentTime;
  final String appointmentQue;
  final String serviceUnitName;

  Appointment({
    required this.appointmentNo,
    required this.appointmentDate,
    required this.status,
    required this.paramedicName,
    required this.appointmentTime,
    required this.appointmentQue,
    required this.serviceUnitName,
  });

}
